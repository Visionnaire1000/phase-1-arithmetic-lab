
// Set num1 and num2 such that their product is 62
let num1 = 31;
let num2 = 2;
let multiply = num1 * num2; // multiply = 62

// Generate a random integer greater than 0
let random = Math.floor(Math.random() * 100) + 1; // Random integer greater than 0

// Set num3 and num4 such that the remainder is 4
let num3 = 16;
let num4 = 6;
let mod = num3 % num4; // mod = 4

// Find the maximum number in a set
let max = Math.max(5, 10, 15, 20); // max = 20

// Log the results
console.log("multiply:", multiply); // 62
console.log("random:", random); // Random integer > 0
console.log("mod:", mod); // 4
console.log("max:", max); // 20
